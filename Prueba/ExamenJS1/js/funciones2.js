function Mostrar(){
    document.getElementById("E1").style.fontWeight = "bold";
    document.getElementById("E2").style.fontSize ="32px";
    document.getElementById("E3").style.color ="red";
}

function Ocultar(){
    document.getElementById("E1").style.fontWeight = "normal";
    document.getElementById("E2").style.fontSize ="";
    document.getElementById("E3").style.color ="";
}